package program;

public class Calculos {

	public Integer soma (Integer num1, Integer num2) {
		
		return num1 + num2;
	}
	public Integer subtrai (Integer num1, Integer num2) {
		
		return num1 - num2;
		
	}
	public Integer multiplica (Integer num1, Integer num2) {
		
		return num1 * num2;
	}
	public Integer divide (Integer num1, Integer num2) {
		
		return num1 / num2;
	}
}
